"""
HID 报告编码器 v2.2.0
严格遵循官方 EasyCon 软件抓包数据实现。
支持所有单按键、十字键、摇杆极值的触发与释放。
新增 L3 (0x0400) 和 R3 (0x0800) 的单按键映射。
"""

from core.utils.logger import get_logger


class HIDReportBuilder:
    """HID 报告构建器，基于官方完整映射表"""

    def __init__(self):
        self.logger = get_logger("HIDReportBuilder")

    # 释放状态包（全键释放）
    RELEASE_PACKET = bytes([0x00, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80])

    # 单按键按下映射（掩码 -> 数据包）
    SINGLE_BUTTON_PRESS = {
        0x0001: bytes([0x00, 0x00, 0x21, 0x08, 0x04, 0x02, 0x01, 0x80]),  # Y
        0x0002: bytes([0x00, 0x00, 0x41, 0x08, 0x04, 0x02, 0x01, 0x80]),  # B
        0x0004: bytes([0x00, 0x01, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # A
        0x0008: bytes([0x00, 0x02, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # X
        0x0010: bytes([0x00, 0x04, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # L
        0x0020: bytes([0x00, 0x08, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # R
        0x0040: bytes([0x00, 0x10, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # ZL
        0x0080: bytes([0x00, 0x20, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # ZR
        0x0100: bytes([0x00, 0x40, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # MINUS
        0x0200: bytes([0x01, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # PLUS
        0x0400: bytes(
            [0x02, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]
        ),  # L3 / LCLICK (补充)
        0x0800: bytes(
            [0x04, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]
        ),  # R3 / RCLICK (补充)
        0x1000: bytes([0x08, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # HOME
        0x2000: bytes([0x10, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0x80]),  # CAPTURE
    }

    # 十字键按下映射（HAT值 -> 数据包）
    HAT_PRESS = {
        0: bytes([0x00, 0x00, 0x00, 0x08, 0x04, 0x02, 0x01, 0x80]),  # 上
        1: bytes([0x00, 0x00, 0x00, 0x18, 0x04, 0x02, 0x01, 0x80]),  # 右上
        2: bytes([0x00, 0x00, 0x00, 0x28, 0x04, 0x02, 0x01, 0x80]),  # 右
        3: bytes([0x00, 0x00, 0x00, 0x38, 0x04, 0x02, 0x01, 0x80]),  # 右下
        4: bytes([0x00, 0x00, 0x00, 0x48, 0x04, 0x02, 0x01, 0x80]),  # 下
        5: bytes([0x00, 0x00, 0x00, 0x58, 0x04, 0x02, 0x01, 0x80]),  # 左下
        6: bytes([0x00, 0x00, 0x00, 0x68, 0x04, 0x02, 0x01, 0x80]),  # 左
        7: bytes([0x00, 0x00, 0x00, 0x78, 0x04, 0x02, 0x01, 0x80]),  # 左上
    }

    # 左摇杆极值映射（(lx, ly) -> 数据包，rx, ry 为默认 128）
    LSTICK_PRESS = {
        (0, 128): bytes([0x00, 0x00, 0x01, 0x00, 0x04, 0x02, 0x01, 0x80]),  # 左
        (255, 128): bytes([0x00, 0x00, 0x01, 0x0F, 0x7C, 0x02, 0x01, 0x80]),  # 右
        (128, 0): bytes([0x00, 0x00, 0x01, 0x08, 0x00, 0x02, 0x01, 0x80]),  # 上
        (128, 255): bytes([0x00, 0x00, 0x01, 0x08, 0x07, 0x7E, 0x01, 0x80]),  # 下
    }

    # 右摇杆极值映射（(rx, ry) -> 数据包，lx, ly 为默认 128）
    RSTICK_PRESS = {
        (0, 128): bytes([0x00, 0x00, 0x01, 0x08, 0x04, 0x00, 0x01, 0x80]),  # 左
        (255, 128): bytes([0x00, 0x00, 0x01, 0x08, 0x04, 0x03, 0x7F, 0x80]),  # 右
        (128, 0): bytes([0x00, 0x00, 0x01, 0x08, 0x04, 0x02, 0x00, 0x80]),  # 上
        (128, 255): bytes([0x00, 0x00, 0x01, 0x08, 0x04, 0x02, 0x01, 0xFF]),  # 下
    }

    def build(
        self,
        buttons: int = 0,
        lx: int = 128,
        ly: int = 128,
        rx: int = 128,
        ry: int = 128,
        hat: int = 8,
    ) -> bytes:
        """
        构建 8 字节 HID 报告。
        匹配优先级：完全释放 > 单按键 > 十字键 > 左摇杆 > 右摇杆。
        未匹配则返回释放包并记录警告。
        """
        # 1. 完全释放状态
        if (
            buttons == 0
            and lx == 128
            and ly == 128
            and rx == 128
            and ry == 128
            and hat == 8
        ):
            return self.RELEASE_PACKET

        # 2. 单按键（摇杆默认，无十字键）
        if lx == 128 and ly == 128 and rx == 128 and ry == 128 and hat == 8:
            if buttons in self.SINGLE_BUTTON_PRESS:
                return self.SINGLE_BUTTON_PRESS[buttons]

        # 3. 十字键（无按键，摇杆默认）
        if (
            buttons == 0
            and lx == 128
            and ly == 128
            and rx == 128
            and ry == 128
            and hat in self.HAT_PRESS
        ):
            return self.HAT_PRESS[hat]

        # 4. 左摇杆极值（无按键，无十字键，右摇杆默认）
        if buttons == 0 and hat == 8 and rx == 128 and ry == 128:
            key = (lx, ly)
            if key in self.LSTICK_PRESS:
                return self.LSTICK_PRESS[key]

        # 5. 右摇杆极值（无按键，无十字键，左摇杆默认）
        if buttons == 0 and hat == 8 and lx == 128 and ly == 128:
            key = (rx, ry)
            if key in self.RSTICK_PRESS:
                return self.RSTICK_PRESS[key]

        # 未匹配任何已知映射
        self.logger.warning(
            f"未找到匹配的 HID 报告映射: buttons=0x{buttons:04X}, "
            f"LX={lx}, LY={ly}, RX={rx}, RY={ry}, HAT={hat}，将发送释放状态包"
        )
        return self.RELEASE_PACKET
